// Laços de repetição WHILE
// NÃO EXECUTA SE A CONDIÇÃO FOR FALSE

let contador = 1 

while(contador <= 10){
    console.log('WHILE! Numero atual: ', contador)
    contador++
}

// Laços de repetição DO WHILE

// EXECUTA PRIMEIRO E DEPOIS VERIFICA SE É TRUE 

let numero = 1

do{
    console.log('DO WHILE! numero atual: ', numero)
    numero++
}while(numero <= 10)

