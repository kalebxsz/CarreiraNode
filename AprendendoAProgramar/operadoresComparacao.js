const minhaIdade = 23
const idadeIrmao = 23

// igualdade
let saoIguais = minhaIdade == idadeIrmao
console.log('São iguais? ', saoIguais)

// maior que 
let eMaior = minhaIdade > idadeIrmao
console.log('é maior? ', eMaior)

// menor que 
let eMenor = minhaIdade < idadeIrmao
console.log('é menor? ', eMenor)

// Maior ou igual que
const maiorIdade = minhaIdade >= 18
console.log('sou maior de idade? ', maiorIdade)

// Menor ou igual
const souMenor = minhaIdade <= 18
console.log('sou menor de idade? ', souMenor)

//Estritamente igual 
const estritamenteIgual = minhaIdade === idadeIrmao
console.log('É estritamente igual? ', estritamenteIgual);

// diferente de
let diferente = minhaIdade != idadeIrmao
console.log('é diferente? ', diferente);

//Estritamente diferente
let diferencaEstrita = minhaIdade !== idadeIrmao
console.log('é estritamente diferente? ', diferencaEstrita)